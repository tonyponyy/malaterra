    function mergeArrays(baseArray, overlayArray) {
        if (baseArray.length !== overlayArray.length) {
            throw new Error("Los arrays deben tener la misma longitud");
        }
        
        return baseArray.map((value, index) => overlayArray[index] !== 0 ? overlayArray[index] : value);
    }


